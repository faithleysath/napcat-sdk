    s1 = re.sub('(.)([A-Z][a-z]+)', r'\1_\2', name)
    
    # ç¬¬äºŒæ­¥ï¼šæŠŠ "UserIdentifier" å˜æˆ "User_Identifier"
    # é€»è¾‘ï¼šæ‰¾åˆ°"å°å†™æˆ–æ•°å­—"ï¼Œåé¢è·Ÿç€"ä¸€ä¸ªå¤§å†™"ï¼Œåœ¨å®ƒä»¬ä¸­é—´åŠ ä¸‹åˆ’çº¿
